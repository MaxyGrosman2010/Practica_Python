###Type Hints###

# They are something added to Python that, set the variable to a type, this doesn't mean 
# it's strongtyping and can't be changed. But that we are saying what it's type should be

my_typed_variable: str = "Hello World!"
print(my_typed_variable)
print(type(my_typed_variable))

my_typed_variable = 5
print(my_typed_variable)
print(type(my_typed_variable))